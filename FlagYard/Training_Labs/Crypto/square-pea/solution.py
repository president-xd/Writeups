# ── Step 1: Recover p via GCD trick ─────────────────────────────────────────
# h1 + h2 ≡ 2·e·q^(e-1)·p (mod p²)  →  divisible by p but not p²
p = gcd(h1 + h2, n)
assert n % (p * p) == 0, "p is wrong"
q = n // (p * p)
print(f"[+] p = {p}")
print(f"[+] q = {q}")

# ── Step 2: p-adic discrete log  ─────────────────────────────────────────────
# L(x) = (x^(p-1) - 1) / p  mod p   is a group homomorphism (Z/p²Z)* → Z/pZ
# L(e^flag) = flag·L(e) ≡ L(c)  (mod p)
# → flag ≡ L(c) · L(e)^-1  (mod p)
# Since flag < p (248-bit flag, 256-bit p), this gives the exact flag.

p2 = p * p
Lc = (pow(c, p - 1, p2) - 1) // p   # L(c) mod p
Le = (pow(e, p - 1, p2) - 1) // p   # L(e) mod p

flag = Lc * pow(Le, -1, p) % p

# ── Step 3: Decode ────────────────────────────────────────────────────────────
flag_bytes = long_to_bytes(flag)
print(f"[+] flag = {flag_bytes}")

# Sanity check: re-encrypt and verify
assert pow(e, flag, n) == c, "Verification failed!"
print("[+] Verified: pow(e, flag, n) == c ✓")