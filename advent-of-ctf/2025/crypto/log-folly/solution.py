def recover_flag(p, g, leaks):
    n = len(leaks)
    M = p - 1

    # base A = g^{-(256^n - 1)} mod p   (exponents are mod p-1)
    t = (pow(256, n, M) - 1) % M
    A = pow(g, (-t) % M, p)

    # precompute A^b for b in [0..255]
    table = {pow(A, b, p): b for b in range(256)}

    out = []
    for i in range(n):
        h_i = leaks[i]
        h_next = leaks[(i + 1) % n]

        # ratio = h_{i+1} * (h_i^{256})^{-1} = A^{byte_i}
        denom = pow(h_i, 256, p)
        ratio = (h_next * pow(denom, -1, p)) % p

        b = table[ratio]
        out.append(b)

    return bytes(out)

